class VSSM(nn.Module):
    def __init__(self, img_size=192, patch_size=4, in_chans=3, num_classes=3, depths=[2, 2, 6, 2],
                 depths_decoder=[2, 6, 2, 2],
                 dims=[96, 192, 384, 768], dims_decoder=[768, 384, 192, 96], d_state=16, drop_rate=0.,
                 attn_drop_rate=0., drop_path_rate=0.1,
                 norm_layer=nn.LayerNorm, patch_norm=True,
                 use_checkpoint=False, dims_PARF=[384, 192, 96],
                 reduction=8,
                 **kwargs):
        super().__init__()
        self.pretrain_img_size = img_size
        self.num_classes = num_classes
        self.num_layers = len(depths)
        if isinstance(dims, int):
            dims = [int(dims * 2 ** i_layer) for i_layer in range(self.num_layers)]
        self.embed_dim = dims[0]
        self.num_features = dims[-1]
        self.dims = dims

        self.reduction = reduction

        self.patch_embed = PatchEmbed2D(patch_size=patch_size, in_chans=in_chans, embed_dim=self.embed_dim,
                                        norm_layer=norm_layer if patch_norm else None)

        
        self.ape = False
        
        if self.ape:
            self.patches_resolution = self.patch_embed.patches_resolution
            self.absolute_pos_embed = nn.Parameter(torch.zeros(1, *self.patches_resolution, self.embed_dim))
            trunc_normal_(self.absolute_pos_embed, std=.02)
        self.pos_drop = nn.Dropout(p=drop_rate)

        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]  # stochastic depth decay rule
        dpr_decoder = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths_decoder))][::-1]

        self.layers = nn.ModuleList()
        for i_layer in range(self.num_layers):
            layer = VSSLayer(
                dim=dims[i_layer],
                depth=depths[i_layer],
                d_state=math.ceil(dims[0] / 6) if d_state is None else d_state,  # 20240109
                drop=drop_rate,
                attn_drop=attn_drop_rate,
                drop_path=dpr[sum(depths[:i_layer]):sum(depths[:i_layer + 1])],
                norm_layer=norm_layer,
                downsample=PatchMerging2D if (i_layer < self.num_layers - 1) else None,
                use_checkpoint=use_checkpoint,
                reduction=reduction,

            )
            self.layers.append(layer)

        # 20240328这里加上了decoder部分
        self.layers_up = nn.ModuleList()
        for i_layer in range(self.num_layers):
            layer = VSSLayer_up(
                dim=dims_decoder[i_layer],
                depth=depths_decoder[i_layer],
                d_state=math.ceil(dims[0] / 6) if d_state is None else d_state,  # 20240109
                drop=drop_rate,
                attn_drop=attn_drop_rate,
                drop_path=dpr_decoder[sum(depths_decoder[:i_layer]):sum(depths_decoder[:i_layer + 1])],
                norm_layer=norm_layer,
                upsample=PatchExpand2D if (i_layer != 0) else None,
                use_checkpoint=use_checkpoint,
                reduction=reduction,
                PARF_dim=dims_PARF
            )
            self.layers_up.append(layer)

        self.final_up = Final_PatchExpand2D(dim=dims_decoder[-1], dim_scale=4, norm_layer=norm_layer)
        self.final_conv = nn.Conv2d(dims_decoder[-1] // 4, 3, 1)
        self.final_conv_BN = nn.BatchNorm2d(3)

        self.fu0 = PARF(in_chan=dims_PARF[0], mid_chan=dims_PARF[0] // 2)
        self.fu1 = PARF(in_chan=dims_PARF[1], mid_chan=dims_PARF[1] // 2)
        self.fu2 = PARF(in_chan=dims_PARF[2], mid_chan=dims_PARF[2] // 2)
        self.dims_PARF = dims_PARF

      

        self.apply(self._init_weights)
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='leaky_relu')

    def _init_weights(self, m: nn.Module):
       
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.BatchNorm2d):
            nn.init.constant_(m.weight, 1.0)  # BatchNorm权重初始化为1
            nn.init.constant_(m.bias, 0)  # BatchNorm偏差初始化为0

    @torch.jit.ignore
    def no_weight_decay(self):
        return {'absolute_pos_embed'}

    @torch.jit.ignore
    def no_weight_decay_keywords(self):
        return {'relative_position_bias_table'}

    def forward_features(self, x):  
        skip_list = []
        x = self.patch_embed(x)  
        if self.ape:  
            x = x + self.absolute_pos_embed
        x = self.pos_drop(x)  
        
        for layer in self.layers:
            skip_list.append(x)  
            x = layer(x)  
        return x, skip_list  

    
    def forward_features_up(self, x, skip_list):
        cmt = 0
        for inx, layer_up in enumerate(self.layers_up):
            x = layer_up(x)
            if inx > 0 and cmt < 3:
                skip_iner = skip_list[2 - cmt][:, 2 ** (cmt + 1):10 * 2 ** cmt, 2 ** (cmt + 1):10 * 2 ** cmt, :]
                skip_iner = skip_iner.permute(0, 3, 1, 2)
                x_up_iner = x[:, 2 ** (cmt + 1):10 * 2 ** cmt, 2 ** (cmt + 1):10 * 2 ** cmt, :]
                x_up_iner = x_up_iner.permute(0, 3, 1, 2)

               
                if skip_iner.size(1) == self.dims_PARF[0]:
                    current_parf = self.fu0
                elif skip_iner.size(1) == self.dims_PARF[1]:
                    current_parf = self.fu1
                else:  # 假设只有三种可能的通道数
                    current_parf = self.fu2

                merge = current_parf(skip_iner, x_up_iner)

                # 创建一个与x形状相同的新变量
                new_x = x.clone()
                # 修改新变量的相应部分
                new_x[:, 2 ** (cmt + 1):10 * 2 ** cmt, 2 ** (cmt + 1):10 * 2 ** cmt, :] = merge
                # 使用新变量继续后续计算
                x = new_x

                cmt = cmt + 1

        return x


    def forward_final(self, x):
        x = self.final_up(x)
        x = x.permute(0, 3, 1, 2)
        x = self.final_conv(x)
        x = self.final_conv_BN(x)
        return x


    def forward(self, x):
        x, skip_list = self.forward_features(x)

        x[:, 1:5, 1:5, :] = skip_list[3][:, 1:5, 1:5, :]

        x = self.forward_features_up(x, skip_list)
        x = self.forward_final(x)

        return x


